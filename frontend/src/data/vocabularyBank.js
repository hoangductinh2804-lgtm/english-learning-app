function slugify(text) {
  return text
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9\s-]/g, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-");
}

function getWordImage(word, topicCover) {
  const image = `/images/vocabulary-cards/${slugify(word)}.svg`;
  return image;
}

function normalizeWord(topicSlug, wordRecord, index, topicCover) {
  const [word, ipa, meaning, example, imageHint] = wordRecord;
  return {
    _id: `${topicSlug}-${index}-${slugify(word)}`,
    word,
    ipa,
    meaning,
    example,
    image: imageHint ? `/images/vocabulary-cards/${imageHint}.svg` : getWordImage(word, topicCover),
  };
}

function mergeTopicWords(topicSlug, topicWords) {
  const extras = b2VocabularyExtras[topicSlug] || [];
  const mergedWords = [...topicWords, ...extras];
  const seen = new Set();

  return mergedWords.filter((wordRecord) => {
    const normalizedKey = slugify(wordRecord[0]);
    if (seen.has(normalizedKey)) {
      return false;
    }

    seen.add(normalizedKey);
    return true;
  });
}

export function buildCategories() {
  return topicVocabularyBank.map((topic) => {
    const mergedWords = mergeTopicWords(topic.slug, topic.words);

    return {
      slug: topic.slug,
      icon: topic.icon,
      cover: topic.cover,
      title: topic.title,
      description: topic.description,
      level: topic.level || "B2",
      flashcardsTotal: mergedWords.length,
      listeningTotal: 1,
      words: mergedWords.map((wordRecord, index) => normalizeWord(topic.slug, wordRecord, index, topic.cover)),
    };
  });
}

export function buildStudyDecks(categories) {
  return Object.fromEntries(categories.map((category) => [category.slug, category.words]));
}

export function buildListeningLessons(categories) {
  return categories.map((category) => {
    const sampleWords = (category.words || []).slice(0, 6);
    const focusWords = sampleWords.slice(0, 3).map((word) => word.word).join(", ");

    return {
      id: `${category.slug}-listening`,
      title: `${category.title} Listening`,
      topic: category.title,
      level: category.level || "B2",
      duration: "3m 00s",
      highlight: `Luyện nghe từ vựng chủ đề ${category.title}.`,
      script: [
        `Today we practice ${category.title}.`,
        focusWords ? `Useful words include ${focusWords}.` : `Useful words appear in this topic.`,
        "Listen carefully and repeat the words aloud.",
        "Try to remember the meaning and use each word in a sentence.",
      ],
      questions: [
        {
          prompt: `Which topic is this lesson about?`,
          options: [category.title, "Daily Life", "Technology"],
          answer: 0,
        },
        {
          prompt: `What should you do while listening?`,
          options: ["Repeat aloud", "Sleep", "Ignore the words"],
          answer: 0,
        },
        {
          prompt: `How many sample words are shown?`,
          options: ["One", "Two", "Three or more"],
          answer: 2,
        },
      ],
      vocabulary: sampleWords.map((word) => ({
        word: word.word,
        ipa: word.ipa,
        meaning: word.meaning,
        example: word.example,
        image: word.image,
      })),
    };
  });
}

